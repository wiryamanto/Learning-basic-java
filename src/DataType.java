public class DataType {
    public static void main(String[] args){
//        Tipe data Primitive :
//        Integer, Boolean, Char, Byte, Float, BigInterger, Double
//        Contoh:

//        Tipe data Integer:
        int angka1 = 4;
        int angka2 = 8;
        int angka3 = 10;
        int umur = 18;
        System.out.println(angka1);
        System.out.println(angka2);
        System.out.println(angka3);

//        Tipe Data Boolean:
        boolean pilih = false;
        boolean maju = true;
        System.out.println(pilih);
        System.out.println(maju);

//        Tipe Data Char (Karakter) / 1 Huruf
        char inisial_nama = 'R';
        char kode = 'E';
        char Jenis_kelamin = 'L';
        System.out.println(inisial_nama);
        System.out.println(kode);
        System.out.println(Jenis_kelamin);

//        Tipe Data Byte:
        byte number1 = 0;
        byte number2 = 6;
//        byte kilometer = 247; // jika ingin menggunakan tipe data byte seharusnya hanya menyimpang angka dalam kisaran -128 sampai + 127
        byte number3 = 56;
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);


//        Tipe data Float:
        float berat_badan = 58.7f;
        float tonase = 7.0f;
        float nilai_matematika = 59f;
        System.out.println(berat_badan);
        System.out.println(tonase);
        System.out.println(nilai_matematika);

//        Tipe Data Double
        double waktu_tempuh = 4.5;
        double nilai_Ipa = 89.8;
        System.out.println(waktu_tempuh);
        System.out.println(nilai_Ipa);
    }
}
